				<center><h2>ABOUT US</h2></center><br>
                <p>The 'Moto-Fine Handler', is an attempt to design a system
which will automatically incur penalty or case to the driver and the owner for
violation of traffic rules. Though there are different penalties available for
violating different traffic rules, still a large number of offences go unregistered.
The main reason for that is bribery and Corruption. Due to bribery, the money that
should be collected as fine goes into the pocket of corrupted officers and as there is
no centralized system available, it is difficult to keep track of repeated offenders.
This is where there is a need for a system to curb this corruption and thus gave us
the opportunity to create this system. The system stores the vehicle information
hence just using the vehicle number he can get all the information about the
vehicle owner. In existing system Motor Vehicle Inspector and Sub-Inspector in
charge of the Traffic Police wing have the right to levy on-the-spot fines from the
violators. Fines would be paid on the spot and a receipt would be given to the
driver. Other officers issue a Vehicle Check report which lists out the violations
noticed on the driver or on the vehicle. The notice would contain the relevant
sections in which the driver is charged and also a date to appear in court. Also the
users can pay the fine amount online through this system and also have the
provision of applying Vehicle Register Certificate Application Form. In addition to
this, this system allows us to give request for changing the data in registration
certificate and in renewing the insurance.</p>
